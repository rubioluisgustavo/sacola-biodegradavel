          <div class="col-lg-12 text-center">
            <h3>Sustentabilidade</h3>
            <p>O Brasil produz cerca de 240 mil t de lixo por dia – número inferior ao dos EUA (607 t/dia), mas bem
              superior ao de países como a Alemanha (85 t/dia) e a Suécia (10,4 t/dia). Desse total, a maior parte vai
              parar nos lixões a céu aberto; apenas uma pequena porcentagem é levada para locais apropriados. Uma cidade
              como São Paulo gasta, por dia, 1 milhão de reais com a questão do lixo. São poucas as prefeituras do país
              que possuem equipes e políticas públicas específicas para o lixo. Quando ele não é tratado, constitui-se
              num sério problema sanitário, pois expõe as pessoas a várias doenças (diarréia, amebíase, parasitose) e
              contamina o solo, as águas e os lençóis freáticos.</p>

            <p>Entre as soluções para a questão estão a criação de aterros sanitários em locais adequados, a adoção de
              programas de coleta seletiva e reciclagem, a realização de campanhas de conscientização da sociedade e uma
              maior atuação dos poderes públicos. O lixo pode ser classificado de acordo com sua natureza física,
              composição química, origem, riscos potenciais ao meio ambiente, entre outros fatores. A maior parte do
              lixo domiciliar no Brasil é composta de matéria orgânica; em seguida vem o papel.</p>

            <p>O tratamento adequado do lixo envolve tanto vantagens ambientais (preservação, saúde e qualidade de vida)
              como econômicas. O consumo de energia e de água no processo de reciclagem do papel, por exemplo, é 50%
              menor que o verificado na produção do material novo. Tem crescido também a preocupação com materiais
              tóxicos, como pilhas, baterias de telefone celular e pneus. Quando descartados de forma irregular, esses
              objetos ampliam os problemas sanitários e de contaminação. As pilhas, por exemplo, deixam vazar metais
              como o zinco e o mercúrio, extremamente prejudiciais à saúde. Os pneus, ao acumular água, transformam-se
              em focos de doenças, como a dengue e a malária.</p>

            <p>Desde julho de 1999, uma resolução do Conselho Nacional do Meio Ambiente (Conama) responsabiliza os
              fabricantes e comerciantes pelo destino final desse tipo de produto, depois que forem descartados pelos
              usuários. O governo federal também pretende implantar a Política Nacional de Resíduos Sólidos, que tem por
              objetivo estimular o uso dos resíduos de maneira sustentável, pela redução do consumo, da reciclagem e da
              reutilização de materiais.</p>
